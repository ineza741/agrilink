export const DEFAULT_FARM_IMAGE = FARM_LANDSCAPE;

const cropAliases = {
  bean: "Beans",
  "bush bean": "Beans",
  "bush beans": "Beans",
  corn: "Maize",
  "hybrid corn": "Maize",
  potato: "Potato",
  "irish potato": "Potato",
  almond: "Almonds",
};

export function getFarmImage(farm) {
  if (!farm) return DEFAULT_FARM_IMAGE;
  if (farm.photoUrl) return farm.photoUrl;
  if (farm.imageUrl) return farm.imageUrl;
  const crop = farm.primaryCrop || "";
  if (crop) {
    const trimmed = crop.trim();
    const alias = cropAliases[trimmed.toLowerCase()];
    const heroUrl = getFarmHeroImageUrl(alias || trimmed);
    if (heroUrl) return heroUrl;
  }
  return DEFAULT_FARM_IMAGE;
}

export function getCropImageUrl(cropName) {
  if (!cropName) return null;
  const name = cropName.trim();
  if (cropImages[name]) return cropImages[name];
  const lower = name.toLowerCase();
  for (const [key, url] of Object.entries(cropImages)) {
    if (lower.includes(key.toLowerCase())) return url;
  }
  return cropImages.Maize;
}

